package pickit;

/**
 *
 * @author MorganWillis
 */
public class finger {
    int fret;
    int string;
    String symbol;
    
    public int getFret() {
        return fret;
    }
    
    public void setFret(int newFret) {
        fret = newFret;
    }
      
    public int getString() {
        return string;
    }
    
    public void setString(int newString) {
        string = newString;
    }
    
    public String getSymbol() {
        return symbol;
    }
    
    public void setSymbol(String newSymbol) {
        symbol = newSymbol;
    }
    
    
}
